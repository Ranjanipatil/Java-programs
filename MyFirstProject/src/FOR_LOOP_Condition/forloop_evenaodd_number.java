package FOR_LOOP_Condition;

public class forloop_evenaodd_number {

	private static int i = 0;

	public static void main(String[] args) {
		for (i=0;i<=100;i++)
		{
			if(i%2==0)
			
				System.out.println(i+"is even");
			
			else 
			
				System.out.println(i+"is odd");
			
	}
		
	}

}
