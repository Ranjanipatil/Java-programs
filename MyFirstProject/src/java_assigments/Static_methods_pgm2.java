package java_assigments;
//Create 4 static method with different name and call it inside the main method

public class Static_methods_pgm2{
	static void method1()//static method1
	{
		System.out.println("method1");
	}
	static void method2()//static method 2
	{
		System.out.println("method2");
	}
	static void method3()//static method 3
	{
		System.out.println("method3");
	}
	static void method4()//static method 4
	{
		System.out.println("method4");
	}
	public static void main(String[] args) //main method
	{
		method1();
		method2();
		method3();
		method4();

	}

}
